// define a user model

const mongoose = require("mongoose");

// trim removes leading and trailing white spaces during user input

const userSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
    trim: true,
  },
  lastName: {
    type: String,
    required: true,
    trim: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
    enum: ["Customer", "Restaurateur"],
  },
});

const User = mongoose.model("User", userSchema);

module.exports = User;